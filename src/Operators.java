public class Operators {

    public static void main(String[] args) {

        // Operators

        // Assignment Op   -> =
        // Sum             -> +
        // Subtract        -> -
        // Division        -> /
        // Multiply        -> *
        // Remainder       -> %
        // Auto Increment  -> ++
        // Auto Decrement  -> --


        int i = 1;

        // Unary Operator
        i++;

        System.out.println(i);

        i--;

        System.out.println(i);


    }

}
