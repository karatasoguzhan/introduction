public class AugmentedAssignment {


    public static void main(String[] args) {

        int value;
        value = 15;

        System.out.println(value);

        value = 10 + value;

        System.out.println(value);

        int value2 = 20;

        value2 = value2 / 2;

        int value3 = 30;

        value3 = value3 * 3;

        // augmented assignments

        int value4 = 50;

        value4 +=5; // value4 = value4 + 5
        value4 -=5;
        value4 *=5;
        value4 /= 5;
        value4 %=5;





    }

}
